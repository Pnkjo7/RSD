package com.rsd;

public class ContactCol {
	private String name,email,message;
	
	public ContactCol() {
		super();
	}
	
	public ContactCol(String name, String email, String message) {
		super();
		this.name = name;
		this.email = email;
		this.message = message;
	}
	
	public String getName() {
		return name;
	}
	
	public String setName() {
		return name;
	}
	
	public String getEmail() {
		return email;
	}
	
	public String setEmail() {
		return email;
	}
	
	public String getMessage() {
		return message;
	}
	
	public String setMessage() {
		return message;
	}


}
