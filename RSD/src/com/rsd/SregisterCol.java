package com.rsd;

public class SregisterCol {
	private String uname,pass;

	
	public SregisterCol() {
		super();
	}


	public SregisterCol(String uname, String pass) {
		super();
		this.uname = uname;
		this.pass = pass;
			}


	public String getuname() {
		return uname;
	}


	public void setuname(String uname) {
		this.uname = uname;
	}


	public String getpass() {
		return pass;
	}


	public void setpass(String pass) {
		this.pass = pass;
	}




		
}